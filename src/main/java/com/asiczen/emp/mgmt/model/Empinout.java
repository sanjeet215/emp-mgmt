package com.asiczen.emp.mgmt.model;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "employeeinout")
public class Empinout extends AuditModel{

	private static final long serialVersionUID = -4455600578399600101L;

	@Id
	@GeneratedValue(strategy = GenerationType.AUTO)
	Long id;

	String orgId;
	String empId;
	Date timeStamp;
	String type;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getOrgId() {
		return orgId;
	}

	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}

	public String getEmpId() {
		return empId;
	}

	public void setEmpId(String empId) {
		this.empId = empId;
	}

	public Date getTimeStamp() {
		return timeStamp;
	}

	public void setTimeStamp(Date timeStamp) {
		this.timeStamp = timeStamp;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	@Override
	public String toString() {
		return "Empinout [id=" + id + ", orgId=" + orgId + ", empId=" + empId + ", timeStamp=" + timeStamp + ", type="
				+ type + "]";
	}

}
